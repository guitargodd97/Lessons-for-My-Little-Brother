package com.heidenreich.lessons;

public class IntExamples {
	public static void main(String[] args) {

		// Create Variables
		int x = 90;
		int y = 50;
		int z = 0;

		// Set Z to a number
		z = 443;
		System.out.println(z);

		// Set Z to a variable
		z = x;
		System.out.println(z);

		// Addition
		z = x + y;
		System.out.println(z);

		// Subtraction
		z = x - y;
		System.out.println(z);

		// Multiplication
		z = x * y;
		System.out.println(z);

		// Division
		z = x / y;
		System.out.println(z);

		// Modulus
		z = x % y;
		System.out.println(z);

		// Increment
		z = 5;
		z++;
		System.out.println(z);

		// Simple Addition
		z = 5;
		z += 3;
		System.out.println(z);

		// Decrement
		z = 5;
		z--;
		System.out.println(z);

		// Simple Subtraction
		z = 5;
		z -= 3;
		System.out.println(z);

		// Simple Multiplication
		z = 5;
		z *= 19;
		System.out.println(z);

		// Simple Division
		z = 6;
		z /= 2;
		System.out.println(z);
	}
}
